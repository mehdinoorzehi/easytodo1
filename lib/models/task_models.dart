class WorkTaskModels {
  String? title;
  String? subtitle;
  bool? status;

  WorkTaskModels(
      {required this.title, required this.subtitle, required this.status});
  Map<String, dynamic> toJson() {
    return {
      'title': title,
      'subtitle': subtitle,
      'status': status,
    };
  }

  WorkTaskModels.fromJson(Map<String, dynamic> json)
      : title = json['title'],
        subtitle = json['subtitle'],
        status = json['status'];
}

